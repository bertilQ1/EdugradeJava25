public class Uppgift2_3 {
    public static void main(String[] args) {
        int x = 10;
        int y = 5;
        int z = 20;
        System.out.println(x > y && z > y);
        System.out.println(x > y || z < y);
        System.out.println(x > y);
        System.out.println(x + " is larger than " + y);
    }
}
